class CAPP(nn.Module):
    def __init__(self, c1, c2, k=5):
        super().__init__()
        c_ = c1
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv3 = DWConv(c_, c_, 7, 1)
        self.cv4 = DWConv(c_, c_, 7, 1)
        self.cv5 = DWConv(c_, c_, 7, 1)
        self.m = nn.MaxPool2d(k, 1, k//2)
        self.n = nn.AvgPool2d(k, 1, k//2)
        self.cv2 = Conv(c_ * 3, c2, 1, 1)

    def forward(self, x):
        # 通道分块
        input = self.cv1(x)
        # 跨池化交互
        y1 = self.m(input)
        x1 = self.n(input)
        y2 = self.m(y1)
        x2 = self.n(x1)
        y3 = self.m(y2)
        x3 = self.n(x2)

        # 多尺度卷积
        s1 = self.cv3(x1 + y1)
        s2 = self.cv4(x2 + y2)
        s3 = self.cv5(x3 + y3)

        # 拼接 + 通道混洗
        out = torch.cat((s1, s2, s3), dim=1)

        return self.cv2(out)


